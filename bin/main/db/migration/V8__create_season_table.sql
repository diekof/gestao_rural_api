CREATE TABLE seasons (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id UUID NOT NULL,
    field_id UUID NOT NULL,
    crop_id UUID NOT NULL,
    nome VARCHAR(120) NOT NULL,
    ano_safra INTEGER NOT NULL,
    data_inicio DATE NOT NULL,
    data_fim DATE,
    area_plantada NUMERIC(12,2) NOT NULL,
    status VARCHAR(20) NOT NULL,
    previsao_producao NUMERIC(12,2),
    producao_real NUMERIC(12,2),
    observacoes TEXT,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    created_by VARCHAR(100) NOT NULL,
    updated_by VARCHAR(100) NOT NULL,
    CONSTRAINT fk_seasons_tenant FOREIGN KEY (tenant_id) REFERENCES tenants(id),
    CONSTRAINT fk_seasons_field FOREIGN KEY (field_id) REFERENCES fields(id),
    CONSTRAINT fk_seasons_crop FOREIGN KEY (crop_id) REFERENCES crops(id),
    CONSTRAINT ck_seasons_area_plantada_positive CHECK (area_plantada > 0),
    CONSTRAINT ck_seasons_data_periodo CHECK (data_fim IS NULL OR data_fim >= data_inicio),
    CONSTRAINT ck_seasons_previsao_positive CHECK (previsao_producao IS NULL OR previsao_producao > 0),
    CONSTRAINT ck_seasons_real_positive CHECK (producao_real IS NULL OR producao_real > 0)
);
